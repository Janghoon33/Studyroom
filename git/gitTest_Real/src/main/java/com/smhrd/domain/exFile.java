package com.smhrd.domain;

public class exFile {
	private String ex_id;
	private String filename;


	
	public exFile(String ex_id, String filename) {
		super();
		this.ex_id = ex_id;
		this.filename = filename;
		
	}
	
	public String getEx_id() {
		return ex_id;
	}
	
	public String getFilename() {
		return filename;
	}
	

	

}
